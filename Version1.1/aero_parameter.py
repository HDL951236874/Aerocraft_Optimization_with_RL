import math
def aero_parameter(h):
    k = 1.4
    R = 287

    T1 = 40 + 273.15
    p1 = 0.017e4
    pho1 = 0.002

    #气动常数定义
    if (h <= 32e3) and (h >= 20e3):
        T1 = 216.65 + 0.001 * (h - 20000)
        p1 = 5474.86 * (T1 / 216.65) ** (-34.1632)
        pho1 = 0.088035 * (T1 / 216.65) ** (-35.1632)
    elif (h<=20e3)and(h>11e3):
        T1 = 216.65
        p1 = 2.261e4 * math.exp(-(h - 11000) / 6341.62)
        pho1 = 0.364 * math.exp(-(h - 11000) / 6341.62)
    elif (h<=11e3)and(h>=0):
        T1 = 288.15 - 0.0065 * h #海平面参数
        p1 = 101.325e3 * (T1 / 288.15) ** 5.25588
        pho1 = 1.225 * (T1 / 288.15) ** 4.25588
    elif (h<=45e3):
        T1 = 40 + 273.15
        p1 = 0.017e4
        pho1 = 0.002

    a1 = math.sqrt(k * R * T1)

    return T1,a1,p1,pho1
